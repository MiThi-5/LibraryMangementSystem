    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         if(jTextField1.getText().equals("")){
         JOptionPane.showMessageDialog(null, "Field Must not Empty...!");
        }else{
        String B_ID, name,publisher,price,pages;
        B_ID=jTextField1.getText();
        name=jTextField2.getText();
        publisher=jTextField3.getText();
        price=jTextField4.getText();
        pages=jTextField5.getText();
        
         try{
         String query ="Update newbook set Book_ID='"+B_ID+"',name='"+name+"',publisher='"+publisher+"',price='"+price+"',pages='"+pages+"' where Book_ID='"+B_ID+"'";
         pst = con.prepareStatement(query);
         pst.execute();
         refresh();
         clear();
         JOptionPane.showMessageDialog(null, "Book Updated Successfully...!");
         
         }catch(Exception e){
         JOptionPane.showMessageDialog(null, e);
         
         }
        
        }


      
    }//GEN-LAST:event_jButton2ActionPerformed


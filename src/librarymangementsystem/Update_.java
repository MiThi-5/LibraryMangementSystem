    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
         if(jTextField1.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Please insert Book ID or Book Name!");
        
        }else{
        try{
        String query="select * from newbook where Book_ID ='"+jTextField1.getText()+"'";
         pst = con.prepareStatement(query);
         rs=pst.executeQuery();
         jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        
        }catch(Exception e){
         JOptionPane.showMessageDialog(null, "Book does not issue which you searching...!");
        
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       try{
        int row =jTable1.getSelectedRow();
        String BookID=(jTable1.getModel().getValueAt(row,0).toString());
        String query ="select * from newbook where Book_ID='"+BookID+"'";
        pst = con.prepareStatement(query);
         rs=pst.executeQuery();
         
        while(rs.next()){
        jTextField1.setText(rs.getString("Book_ID"));
        jTextField2.setText(rs.getString("name"));
        jTextField3.setText(rs.getString("publisher"));
        jTextField4.setText(rs.getString("price"));
        jTextField5.setText(rs.getString("pages"));
        
        }
        
        }catch(SQLException e){
        
        e.printStackTrace();
        
        }

        
       
    }//GEN-LAST:event_jTable1MouseClicked


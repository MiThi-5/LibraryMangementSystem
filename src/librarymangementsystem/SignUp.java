    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try{
        if (jTextField1.getText().equals("")||jTextField2.getText().equals("")||jPasswordField1.getPassword().equals("")||jComboBox1.getSelectedItem().equals("")||jTextField3.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Field Must Not Empty!!!");
        }else{
        String sql = "select email from signup where email=?";
        pst = con.prepareStatement(sql);
        pst.setString(1,jTextField1.getText());
        rs=pst.executeQuery();
        if(rs.next()){
        JOptionPane.showMessageDialog(null,"This Email already Exists!");
        
        }else{
        String sql1 ="insert into signup(email,username,password,sec_q,answer) values (?,?,?,?,?)";
        pst = con.prepareStatement(sql1);
        pst.setString(1,jTextField1.getText());
        pst.setString(2,jTextField2.getText());
        pst.setString(3,jPasswordField1.getText());
        pst.setString(4, (String) jComboBox1.getSelectedItem());
        pst.setString(5,jTextField3.getText());
        pst.execute();
        clear();
        JOptionPane.showMessageDialog(null,"successfully Created New Account...!!! ");
        
        }
        
        }
        
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
        
        }
            
        
        
  
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        if (jTextField1.getText().equals("")||jTextField2.getText().equals("")||jTextField3.getText().equals("")||jTextField4.getText().equals("")||jTextField5.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Field Must Not Empty!!!");
        }else{
        String sql = "select Book_ID from newbook where Book_ID=?";
        pst = con.prepareStatement(sql);
        pst.setString(1,jTextField1.getText());
        rs=pst.executeQuery();
        if(rs.next()){
        JOptionPane.showMessageDialog(null,"This Book ID already Exists!");
        clear();
        }else{
        String sql1 ="insert into newbook (Book_ID,name,publisher,price,pages) values (?,?,?,?,?)";
        pst = con.prepareStatement(sql1);
        pst.setString(1,jTextField1.getText());
        pst.setString(2,jTextField2.getText());
        pst.setString(3,jTextField3.getText());
        pst.setString(4,jTextField4.getText());
        pst.setString(5,jTextField5.getText());
        pst.execute();
        clear();
        JOptionPane.showMessageDialog(null,"successfully Inserted...!!! ");
        
        }
        
        }
        
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

